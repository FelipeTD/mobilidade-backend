package com.bot.entity.freenow.gains;

public class TotalsDriver {
	
	private Double gross;
	private Double grossWithoutCommission;
	private Double commission;
	private Integer totalTours;
	
	public Double getGross() {
		return gross;
	}
	public void setGross(Double gross) {
		this.gross = gross;
	}
	public Double getGrossWithoutCommission() {
		return grossWithoutCommission;
	}
	public void setGrossWithoutCommission(Double grossWithoutCommission) {
		this.grossWithoutCommission = grossWithoutCommission;
	}
	public Double getCommission() {
		return commission;
	}
	public void setCommission(Double commission) {
		this.commission = commission;
	}
	public Integer getTotalTours() {
		return totalTours;
	}
	public void setTotalTours(Integer totalTours) {
		this.totalTours = totalTours;
	}
	
	
	

}
